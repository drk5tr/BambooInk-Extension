/** Default rank for words not found in the frequency map. */
const DEFAULT_RANK = 99999;

/**
 * Returns the frequency rank of a word (lower = more common).
 * Returns 99999 for words not in the frequency map.
 *
 * The lookup is case-insensitive: it checks the original word first,
 * then falls back to lowercase.
 */
export function getWordFrequency(word: string): number {
  return (
    WORD_FREQ.get(word) ??
    WORD_FREQ.get(word.toLowerCase()) ??
    DEFAULT_RANK
  );
}
